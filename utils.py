def show_image(image_path):
    """
    แสดงภาพจากไฟล์ที่กำหนด
    """
    try:
        img = plt.imread(image_path)
        plt.imshow(img)
        plt.axis("off")
        plt.show()
    except FileNotFoundError:
        print(f"ไม่พบไฟล์ภาพ: {image_path}")

